namespace ZZZ.Framework.Animations
{
    /// <summary>
    /// Условия сравнения.
    /// </summary>
    public enum ParameterCondition
    {
        /// <summary>
        /// Равны.
        /// </summary>
        Equals,

        /// <summary>
        /// Не равны.
        /// </summary>
        NotEquals,

        /// <summary>
        /// Больше.
        /// </summary>
        More,

        /// <summary>
        /// Меньше.
        /// </summary>
        Less,

        /// <summary>
        /// Больше и равны.
        /// </summary>
        MoreAndEquals,

        /// <summary>
        /// Меньше и равны.
        /// </summary>
        LessAndEquals,
    }
}
